package aufgabe1;

public class Druckauftrag {
	
	private int auftrag, anr;
	private String benutzer, dokument, groesse, status;
	
	public Druckauftrag(String user, String dok, String size){
		user = benutzer;
		dok = dokument;
		size = groesse;
		auftrag = anr +1;
		status = "ausfuehrend";
	}
}
